/* ──────────────────────────────────────────────────────────────
   PAGES を 2 ページずつ見開き（左・右）にまとめて BOOK_DATA を作る。
   ・左ページ／右ページで背景色をわずかに変える（左右の区別のため）。
   ・ページ番号は自動採番（先頭は表紙なので "Cover"）。
   ・奇数枚のときは末尾に空白ページを 1 枚足して偶数に揃える。
   ────────────────────────────────────────────────────────────── */
const _LEFT_BG  = '#faf8f2';
const _RIGHT_BG = '#fdfcf8';

// ページ番号を採番（表紙だけ "Cover"）
PAGES.forEach((p, idx) => { p.pageNum = (idx === 0) ? 'Cover' : String(idx); });

// 偶数枚に揃える
const _padded = (PAGES.length % 2 === 0) ? PAGES : [...PAGES, { pageNum: '' }];

const BOOK_DATA = { spreads: [] };
for (let i = 0; i < _padded.length; i += 2) {
    const left  = _padded[i];
    const right = _padded[i + 1];
    left.bg  = _LEFT_BG;
    right.bg = _RIGHT_BG;
    BOOK_DATA.spreads.push({ left, right });
}
