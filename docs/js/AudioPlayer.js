/* ================================================================
   AudioPlayer.js
   ── 社長の自分史 電子ブック ─ 音声再生クラス

   このクラスの責務:
     ・ページめくり音（rustling.mp3）の再生だけを担当する
     ・連続して何度も再生しても重なって耳障りにならないよう、
       再生のたびに音声を「最初から」再生し直す制御を持つ

   このクラスの責務外:
     ・いつ音を鳴らすか（ページめくりのどのタイミングか）の判断
       → BookController が「めくりが確定した瞬間」に play() を
         呼ぶだけで、このクラス自体はページの状態を何も知らない

   ── 音声データの埋め込み方法（初心者向け補足） ──
     本来、音声ファイルは <audio src="rustling.mp3"> のように
     別ファイルとして参照することが多い。しかし、このプロジェクトは
     「全部を1つの HTML ファイルにまとめる」という方針のため、
     音声データそのものを文字列に変換してコードの中に直接埋め込んでいる。

     その変換方法が「データURL（Base64エンコード）」という仕組み。
     画像や音声などのバイナリデータ（0と1の羅列）を、
     英数字だけの文字列に変換する規格を Base64 と呼ぶ。
     "data:audio/mpeg;base64,◯◯◯◯..." という形式の文字列を
     <audio> や Audio オブジェクトの src にそのまま渡すと、
     ブラウザは「これは外部ファイルではなく、文字列自体が
     音声データそのものだ」と理解して再生できる。
     メリットは「別ファイルを用意しなくて良い」こと、
     デメリットは「文字列が長くなり、HTML ファイル自体が
     大きくなる」こと（今回は約38KBの音声なので約51KBの
     文字列になっている）。

   ── 再生のたびに最初から再生し直す理由 ──
     HTMLAudioElement（new Audio()で作るオブジェクト）は、
     一度 play() すると再生中の状態を保持する。
     ページをすばやく連続でめくると、前の再生が終わる前に
     次の play() が呼ばれることがあり、何も対策しないと
     「再生中の音声に対して再度 play() するだけ」になって
     2回目以降の音が鳴らない（または変な位置から再生される）
     ことがある。これを避けるため、再生前に currentTime を 0 に
     戻してから play() することで、毎回「最初から」確実に
     鳴らせるようにしている。
   ================================================================ */

class AudioPlayer {

    /**
     * @param {string} dataUrl - "data:audio/mpeg;base64,..." 形式の音声データURL
     * @param {number} [volume] - 再生音量（0.0〜1.0）。控えめな音量を既定値にしている
     */
    constructor(dataUrl, volume = 0.45) {
        // new Audio(url) で音声オブジェクトを作成する。
        // <audio> タグを HTML に書かずに、JavaScript だけで
        // 音声を扱えるようにするための標準的な書き方。
        this.audio = new Audio(dataUrl);
        this.audio.volume = volume;

        // ページめくり音の ON/OFF 状態。
        // setEnabled() で切り替え、play() の冒頭でこのフラグを
        // 確認することで、「OFF のときは何もしない」を実現する。
        // 既定値は true（音が鳴る）。
        this.enabled = true;

        // 音声ファイルの読み込み自体に失敗した場合（データが壊れている、
        // ブラウザが対応していない形式、サンドボックス化された
        // プレビュー環境で音声再生自体がブロックされている、等）に
        // 備えて、エラーが起きてもアプリ全体が止まらないように
        // イベントリスナーで捕捉しておく。
        //
        // 補足: claude.ai のファイルプレビュー（iframe内）のような
        // サンドボックス環境では、セキュリティ上の理由で data: URL の
        // 音声再生がブロックされることがある。これはコードの問題では
        // なく環境側の制約のため、ファイルをダウンロードして直接
        // ブラウザで開けば問題なく再生される。
        this.audio.addEventListener('error', () => {
            const err = this.audio.error;
            console.warn('[AudioPlayer] 音声の読み込みに失敗しました。ページめくり音は再生されません。',
                'errorCode=', err ? err.code : '不明',
                '(1=ABORTED, 2=NETWORK, 3=DECODE, 4=SRC_NOT_SUPPORTED)');
        });
    }

    /**
     * ページめくり音の ON/OFF を切り替える
     * @param {boolean} enabled - true なら音を鳴らす、false なら鳴らさない
     */
    setEnabled(enabled) {
        this.enabled = enabled;
    }

    /**
     * ページめくり音を再生する
     *
     * 呼び出すたびに必ず最初（0秒地点）から再生し直す。
     * play() は Promise を返す仕様だが、ユーザーが音声を一度も
     * 操作していないブラウザでは再生がブロックされることがある
     * （多くのブラウザの「自動再生防止」機能）。
     * ページめくりボタンのクリックやスワイプ操作の直後に呼ばれる
     * 想定なので通常は問題ないが、念のため失敗時にエラーが
     * コンソールに大量に出ないよう catch() で握りつぶしている。
     */
    play() {
        if (!this.enabled) return; // OFF設定のときは何もしない

        try {
            this.audio.currentTime = 0; // 再生位置を先頭に戻す
            const playPromise = this.audio.play();
            // play() が Promise を返す場合のみ catch を付ける
            // （古いブラウザでは undefined を返すことがあるため）
            if (playPromise && typeof playPromise.catch === 'function') {
                playPromise.catch((err) => {
                    // 自動再生制限やサンドボックス環境などで再生が
                    // ブロックされた場合はここに来る。
                    // ページめくり自体は問題なく動作するべきなので、
                    // ここでは警告を出すだけでアプリを止めない。
                    console.warn('[AudioPlayer] 再生がブロックされました:', err.name, err.message);
                });
            }
        } catch (e) {
            console.warn('[AudioPlayer] 再生中にエラーが発生しました:', e);
        }
    }
}
