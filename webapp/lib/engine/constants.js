/* ================================================================
   constants.js
   ── 社長の自分史 電子ブック ─ 定数定義

   このファイルの責務:
     ・サイズ・速度・閾値などのマジックナンバーに名前を付けて
       一元管理する（チューニング窓口を 1 箇所に集約する）

   このファイルの責務外:
     ・実際の描画・計算ロジック
       → 各クラス（PageContentRenderer 等）が担当する

   なぜ定数を分離するか:
     マジックナンバーをコード中に散在させると「この 550 は何か」が
     不明になる。定数に名前を付けることで意図が明確になり、
     サイズ変更時もこのファイルの修正だけで全体に反映される。

   読み込み順序の注意:
     このファイルは BOOK_CONST という名前空間オブジェクトに
     すべての定数をまとめている。
     index.html では他の .js より先に読み込むこと
     （他のクラスがコンストラクタ実行時に参照するため）。
   ================================================================ */

export const BOOK_CONST = {

    /* ── PC（見開き）モード用定数 ───────────────────────────── */
    PC_W:  1100,            // 見開き全体の Canvas 幅（左右ページ合計）
    PC_H:  680,             // Canvas 高さ

    // get 構文について（JavaScript初心者向けメモ）:
    //   「get PAGE_W() { ... }」は普通の値（プロパティ）ではなく、
    //   「BOOK_CONST.PAGE_W を読み取ったタイミングで関数を実行し、
    //   その戻り値を返す」という特殊な書き方（ゲッター）。
    //   普通に `PAGE_W: this.PC_W / 2` と書くと、オブジェクトを
    //   作っている最中の this が正しく PC_W を指していないため
    //   エラーになる。get を使うことで「使われる瞬間に計算する」
    //   形にして、この問題を避けている。
    //   呼び出す側は普通のプロパティと同じ書き方（BOOK_CONST.PAGE_W）
    //   でアクセスできる（関数呼び出しのように () は付けない）。
    get PAGE_W() { return this.PC_W / 2; },  // 片ページ幅 = 550px
    get SPINE_X() { return this.PAGE_W; },   // 綴じ目の X 座標（PC モードでのページ回転軸）

    /* ── モバイル（シングルページ）モード用定数 ─────────────
     *  MOB_W は PC の片ページと同じ幅にすることで、
     *  PageContentRenderer の出力を PC/Mobile 両方で
     *  そのまま流用できるようにしている。
     * ──────────────────────────────────────────────────────── */
    get MOB_W() { return this.PAGE_W; },
    get MOB_H() { return this.PC_H; },

    /* ── 表示モード切り替えの閾値 ───────────────────────────
     *  768px は一般的なタブレット・スマートフォンの境界値。
     *  この値以下のウィンドウ幅ではモバイルモードに切り替わる。
     * ──────────────────────────────────────────────────────── */
    BREAKPOINT: 768,

    /* ── ページめくりアニメーション制御定数 ─────────────────
     *
     *  NUM_SLICES:
     *    PC モード（見開き）のページを水平方向に何分割して描画するか。
     *    分割数が多いほど曲線が滑らかになるが描画コストも増える。
     *    60 は滑らかさとパフォーマンスのバランスが良い値。
     *
     *  MOBILE_NUM_SLICES:
     *    モバイルモード（1ページ）専用の分割数。
     *    drawMobileCurl は Path2D による輪郭統合描画に変更されており、
     *    分割数を増やしてもシェーディング・ハイライト線の API 呼び出しは
     *    増えない設計のため、PC 版より多くしても比較的軽い。
     *    120 は「滑らかさ重視」の値（60 の倍）。
     *
     *  CURL_DELAY:
     *    下端がアニメーション全体の何割先行してめくれるか。
     *    0.0 → 全スライスが同時にめくれる（折り畳まれたような動き）
     *    0.3 → 下端が 30% 先行（自然な紙めくり感）
     *    0.5 → 上端がめくり始める前に下端が半分完了している
     *    詳細は PageFlipEffect の計算式コメントを参照。
     *
     *  FLIP_MS:
     *    1 回のめくりアニメーションにかける時間（ミリ秒）。
     *    requestAnimationFrame の経過時間をこの値で割って
     *    進捗 t（0.0〜1.0）を算出する。
     * ──────────────────────────────────────────────────────── */
    NUM_SLICES: 60,
    MOBILE_NUM_SLICES: 150,
    CURL_DELAY: 0.30,
    FLIP_MS:    1000,

    /* ── パラパラめくり（目次ジャンプ）の重ね合わせ制御 ───────
     *  目次から離れたページへ飛ぶときは、複数ページを「1枚ずつ
     *  めくり終えてから次」ではなく、開始タイミングを少しずつ
     *  ずらしながら同時進行させ、ひとまとめに自然にめくる。
     *
     *  FLUTTER_SHEET_MS:
     *    1枚のシートがめくり始めてからめくり終わるまでの時間。
     *  FLUTTER_STAGGER_MS:
     *    隣り合うシートの「めくり始め」をどれだけずらすか。
     *    FLUTTER_SHEET_MS より小さくすることで、前のシートが
     *    まだめくれている最中に次のシートが動き出し、複数枚が
     *    重なって舞うような自然なリフル（紙を弾く動き）になる。
     *  FLUTTER_MAX_MS:
     *    遠くへ飛ぶ（枚数が多い）ときに全体が長くなりすぎないよう、
     *    総時間の上限。超える場合は1枚あたりとずらし幅を比例縮小する。
     * ──────────────────────────────────────────────────────── */
    FLUTTER_SHEET_MS:   780,
    FLUTTER_STAGGER_MS: 224,
    FLUTTER_MAX_MS:     2800,

    /* ── パラパラめくり中のカール分割数（負荷軽減用）─────────
     *  リフル中は複数シートが同時にめくれるため、1枚を通常の
     *  NUM_SLICES（60）/ MOBILE_NUM_SLICES（150）で描くと、
     *  同時枚数が最大になる中盤で描画コストが跳ね上がりコマ落ちする。
     *  ドラッグ中の軽量化（PC=30 / Mobile=60）と同じ考え方で、
     *  リフル中は粗い分割に落として滑らかさを保つ。
     * ──────────────────────────────────────────────────────── */
    FLUTTER_PC_SLICES:     28,
    FLUTTER_MOBILE_SLICES: 70,

    /* ── ドラッグ（スワイプ）中のカール分割数（負荷軽減用）─────
     *  指/マウスで直接めくっている間は touchmove/mousemove の頻度に
     *  合わせて毎フレーム描き直すため、通常より粗い分割に落とす。
     *  指を離した後の自動補完アニメーションでは通常の分割に戻る。
     * ──────────────────────────────────────────────────────── */
    DRAG_PC_SLICES:     30,
    DRAG_MOBILE_SLICES: 60,

    /* ── 操作判定のしきい値 ─────────────────────────────────
     *  DIRECTION_THRESHOLD:
     *    指/マウスの「ぶれ」を「めくり操作」と見なし始める移動量(px)。
     *  COMMIT_THRESHOLD:
     *    ドラッグで画面幅の何割を超えたらページ送りを確定するか。
     *  FLUTTER_THRESHOLD:
     *    目次ジャンプで何ページ/見開き以上離れていたらパラパラめくり
     *    （リフル）にするか。これ未満なら通常の1回めくり。
     * ──────────────────────────────────────────────────────── */
    DIRECTION_THRESHOLD: 4,
    COMMIT_THRESHOLD:    0.25,
    FLUTTER_THRESHOLD:   2,

    /* ── 綴じ目（背表紙）の影の幅(px)。BookRenderer.drawSpine が使う ── */
    SPINE_SHADOW_W: 22
};
